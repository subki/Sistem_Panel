    private void createtable() {
        String sql = "SELECT * FROM component "
                + " where item_no like ? "
                + " or name like ? "
                + " or description like ? "
                + " or type like ? "
                + " or brand like ? "
                + " or unit like ? "
                + "ORDER BY item_no";
        DefaultTableModel a = new DefaultTableModel();
        a.addColumn("ID");
        a.addColumn("Nama");
        a.addColumn("Spesifikasi");
        a.addColumn("Tipe");
        a.addColumn("Brand");
        a.addColumn("Satuan"); 
        a.addColumn("Aktual");
        a.addColumn("Jual");
        jTable1.setModel(a);
        try {
            PreparedStatement ps = conec.prepareStatement(sql);
            ps.setString(1, "%"+tfsearch.getText()+"%");
            ps.setString(2, "%"+tfsearch.getText()+"%");
            ps.setString(3, "%"+tfsearch.getText()+"%");
            ps.setString(4, "%"+tfsearch.getText()+"%");
            ps.setString(5, "%"+tfsearch.getText()+"%");
            ps.setString(6, "%"+tfsearch.getText()+"%");
            ResultSet rs=ps.executeQuery();
            while(rs.next()){
                a.addRow(new Object[]{
                    rs.getString("item_no"),
                    rs.getString("name"),
                    rs.getString("description"),
                    rs.getString("type"),
                    rs.getString("brand"),
                    rs.getString("unit"),
                    rs.getString("aktual"),
                    rs.getString("jual")
                });
                    System.out.println("Tabel Sukses");
            }
            new AturKolom(jTable1);
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Tabel Gagal");
        }
    }
