    private void createtable() {
        String sql = "SELECT * FROM petugas "
                + " where nik like ? "
                + " or nama like ? "
                + " or alamat like ? "
                + " or telepon like ? "
                + " or email like ? "
                + "ORDER BY nik";
        DefaultTableModel a = new DefaultTableModel();
        a.addColumn("NIK");
        a.addColumn("Nama");
        a.addColumn("Alamat");
        a.addColumn("Telepon");
        a.addColumn("Email");  
        jTable1.setModel(a);
        try {
            PreparedStatement ps = conec.prepareStatement(sql);
            ps.setString(1, "%"+jTextField1.getText()+"%");
            ps.setString(2, "%"+jTextField1.getText()+"%");
            ps.setString(3, "%"+jTextField1.getText()+"%");
            ps.setString(4, "%"+jTextField1.getText()+"%");
            ps.setString(5, "%"+jTextField1.getText()+"%");
            ResultSet rs=ps.executeQuery();
            while(rs.next()){
                a.addRow(new Object[]{
                    rs.getString("nik"),
                    rs.getString("nama"),
                    rs.getString("alamat"),
                    rs.getString("telepon"),
                    rs.getString("email")
                });
                new AturKolom(jTable1);
                System.out.println("Tabel Customer Sukses");
            }
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Tabel Customer Gagal");
        }
    }
