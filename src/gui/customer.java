    public void createtable() {
        String sql = "SELECT * FROM customer "
                + " where id like ? "
                + " or nama like ? "
                + " or alamat like ? "
                + " or telepon like ? "
                + " or fax like ? "
                + " or pic1 like ? "
                + " or pic2 like ? "
                + "ORDER BY id";
        DefaultTableModel a = new DefaultTableModel();
        a.addColumn("ID");
        a.addColumn("Nama");
        a.addColumn("Alamat");
        a.addColumn("Telepon");
        a.addColumn("Fax");
        a.addColumn("PIC 1");
        a.addColumn("PIC 2");  
        a.addColumn("Action");  
        tabelCutomer.setModel(a);
        try {
            PreparedStatement ps = conec.prepareStatement(sql);
            ps.setString(1, "%"+tfsearch.getText()+"%");
            ps.setString(2, "%"+tfsearch.getText()+"%");
            ps.setString(3, "%"+tfsearch.getText()+"%");
            ps.setString(4, "%"+tfsearch.getText()+"%");
            ps.setString(5, "%"+tfsearch.getText()+"%");
            ps.setString(6, "%"+tfsearch.getText()+"%");
            ps.setString(7, "%"+tfsearch.getText()+"%");
            ResultSet rs=ps.executeQuery();
            while(rs.next()){
                a.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("nama"),
                    rs.getString("alamat"),
                    rs.getString("telepon"),
                    rs.getString("fax"),
                    rs.getString("pic1"),
                    rs.getString("pic2"),
                    ""
                });
                TableColumn kolom = tabelCutomer.getColumn("Action");
                kolom.setCellRenderer(new ButtonsRenderer());
                kolom.setCellEditor(new ButtonsEditor(tabelCutomer));
//                
//                int rows = tabelCutomer.getRowHeight();
//                tabelCutomer.setRowHeight(30);
                System.out.println("Tabel Customer Sukses");
            }
            new AturKolom(tabelCutomer);
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Tabel Customer Gagal");
        }
    }

